void checkPauseRun(MainWindow& win, int& keyboard,unsigned int nFrame)
{
    //implemend Pause
    if ((char)keyboard == 'p')
    {
        //frame.copyTo(frameCpy);
        bPaused = true;
    }

    //Make Frame rate faster
    if ((char)keyboard == '+')
        cFrameDelayms--;
    //Slower
    if ((char)keyboard == '-')
        cFrameDelayms++;




    if ((char)keyboard == 't') //Toggle Tracking
        bTracking = !bTracking;

        while (bPaused && !bExiting)
        {
            int ms = 20;
            struct timespec ts = { ms / 1000, (ms % 1000) * 1000 * 1000 };
            nanosleep(&ts, NULL);
            //Wait Until Key to unpause is pressed
            keyboard = cv::waitKey( 30 );

            if ((char)keyboard == 's')
            {
                bSaveImages = !bSaveImages;
                std::string frameNumberString = std::to_string(nFrame);

                ::saveImage(frameNumberString,gstroutDirCSV,frame);
            }

            if ((char)keyboard == 'r')
            {
                bPaused = false;
                gTimer.start();
            }

            //Toggle Show the masked - where blob id really happens
            if ((char)keyboard == 'm')
                 showMask = !showMask;

            if ((char)keyboard == 'q')
                bExiting = true; //Main Loop Will handle this
                 //break;


            //if ((char)keyboard == 'c')
            cv::imshow(gstrwinName, frame);
            win.showCVimg(frame); //Show On QT Window

        }


    //Toggle Show the masked - where blob id really happens
    if ((char)keyboard == 'm')
             showMask = !showMask;
}

